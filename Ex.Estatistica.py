def analise(x):
      print(f'Média: {round(statistics.mean(x))}'
            f'\nModa: {statistics.mode(x)}'
            f'\nMediana: {statistics.median(x)}')

def primeiro_ou_terceiro_quartil(x, y):
      if y == 0:
            print(f'O Primeiro quartil é {np.percentile(x, 25)}'
                  f'\nO Terceiro quartil é {np.percentile(x, 75)}')
      else:
            print(f'O Terceiro quartil é {np.percentile(x, 75)}')

def assimetria(x):
      pearson = (statistics.mean(x) - statistics.mode(x)) / statistics.stdev(x)

      if pearson == 0:
            print('Distribuição Simétrica')
      elif pearson > 0:
            print('Assimétrico a Direita')
      else:
            print('Assimétrico a Esquerda')


